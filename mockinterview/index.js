class Animal {
  constructor(name) {
    this.name = name;
  }

  speak() {
    console.log(`${this.name} makes a noise.`);
  }
}

class Dog extends Animal {
  constructor(name) {
    super(name);
  }

  speak() {
    console.log(`${this.name} Prem.`);
  }
}

const dog = new Dog("Choudhary");
dog.speak(); // Output: Rex barks.
