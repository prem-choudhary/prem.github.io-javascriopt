// new Date(): Creates a new date object with the current date and time.
let date = new Date();
console.log(date);

// getFullYear(): Returns the year of the specified date.
let date1 = new Date();
console.log(date1.getFullYear()); // 2024

// getMonth(): Returns the month in the specified date (0-11).
let date2 = new Date();
console.log(date2.getMonth()); // 10 (November, as months are 0-indexed)

// getDate(): Returns the day of the month (1-31) for the specified date.
let date3 = new Date();
console.log(date3.getDate()); // 13

// getDay(): Returns the day of the week (0-6) for the specified date.
let date4 = new Date();
console.log(date4.getDay()); // 1 (Monday)

